#ifndef SYSTICK_REGS_H
#define SYSTICK_REGS_H

/**********************************************************************************************************************
 * INCLUDES
 *********************************************************************************************************************/
#include "Std_Types.h"


/**********************************************************************************************************************
 *  GLOBAL CONSTANT MACROS
 *********************************************************************************************************************/
	

#define SYSTICK_BASE_ADDRESS            0xE000E000

#define STCTRL                          *((volatile uint32*)(SYSTICK_BASE_ADDRESS + 0x010))
#define STRELOAD                        *((volatile uint32*)(SYSTICK_BASE_ADDRESS + 0x014))
#define STCURRENT                       *((volatile uint32*)(SYSTICK_BASE_ADDRESS + 0x018))
	

/* BITS Number of STCTRL Register in SysTick Peripheral */ 
#define   ENABLE_BIT              (uint8)(0U)
#define   INTERRUPT_BIT           (uint8)(1U)
#define   CLOCK_SOURCE_BIT        (uint8)(2U)          
#define   COUNT_BIT               (uint8)(16U)

 
#endif  /* SYSTICK_REGS_H */

